#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	t_list	*current;

	current = lst;
	if (!current)
		return ;
	while (current->next != NULL)
	{
		f(current->content);
		current = current->next;
	}
	f((t_list *)current->content);
}
